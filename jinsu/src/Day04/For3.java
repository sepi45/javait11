package Day04;

public class For3 {
	public static void main(String[] args) {
		
		for(int i=0; i<5; i++) {
			System.out.println("안녕");
		}
		int sum=0;
		for(int a=0; a<11; a++) {
			sum+= a;
		}
			System.out.println(sum);
			
			for(int b=10; b>0; b--) {
				System.out.println(b);
			}
			for(int c=0; c<21; c+=2) {
				System.out.println(c);
			}
		}
	}

